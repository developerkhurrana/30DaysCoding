// Print the number from 1 to 5 in console using a loop

console.log("Using a for loop");

for (let i = 1; i <= 5; i++) {
  console.log(i);
}

console.log("Using a while loop");

let i = 1;
while (i <= 5) {
  console.log(i);
  i++;
}

console.log("Using a do while loop");

let j = 1;
do {
  console.log(j);
  j++;
} while (j <= 5);
